export function resolveSocialHref(
  platform: SocialPlatform,
  url: string,
): string {
  if (platform === "email") {
    return url.startsWith("mailto:") ? url : `mailto:${url}`;
  }
  if (
    platform === "rss" ||
    platform === "qq" ||
    url.startsWith("http://") ||
    url.startsWith("https://") ||
    url.startsWith("mqqapi://") ||
    url.startsWith("/")
  ) {
    return url;
  }
  return `https://${url}`;
}

